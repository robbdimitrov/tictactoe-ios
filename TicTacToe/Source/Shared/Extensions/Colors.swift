import UIKit

extension UIColor {
    
    class var main: UIColor {
        return self.init(red: 50/255.0, green: 50/255.0, blue: 50/255.0, alpha: 1)
    }
    
    class var background: UIColor {
        return self.init(red: 250/255.0, green: 250/255.0, blue: 250/255.0, alpha: 1)
    }
    
}
